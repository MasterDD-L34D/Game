class TestCmdGenerateFrontmatter:
    def test_dry_run_does_not_modify(self, tmp_path: Path):
        docs = tmp_path / "docs"
        docs.mkdir()
        md = docs / "bare.md"
        md.write_text("# No Frontmatter\nBody.\n", encoding="utf-8")
        original = md.read_text(encoding="utf-8")

        args = argparse.Namespace(command="generate-frontmatter", dry_run=True, path=None, workstream=None)
        ret = migrator.cmd_generate_frontmatter(args, tmp_path)
        assert ret == 0
        assert md.read_text(encoding="utf-8") == original

    def test_adds_frontmatter(self, tmp_path: Path):
        docs = tmp_path / "docs" / "traits"
        docs.mkdir(parents=True)
        md = docs / "bare.md"
        md.write_text("# Trait Doc\nBody.\n", encoding="utf-8")

        args = argparse.Namespace(command="generate-frontmatter", dry_run=False, path=None, workstream=None)
        with patch.object(migrator, "today_str", return_value="2026-04-14"):
            ret = migrator.cmd_generate_frontmatter(args, tmp_path)
        assert ret == 0
        content = md.read_text(encoding="utf-8")
        assert content.startswith("---\n")
        assert "workstream: dataset-pack" in content
        assert "# Trait Doc" in content

    def test_skips_existing_frontmatter(self, tmp_path: Path):
        docs = tmp_path / "docs"
        docs.mkdir()
        md = docs / "existing.md"
        md.write_text("---\ntitle: Existing\n---\n# Existing\n", encoding="utf-8")
        original = md.read_text(encoding="utf-8")

        args = argparse.Namespace(command="generate-frontmatter", dry_run=False, path=None, workstream=None)
        migrator.cmd_generate_frontmatter(args, tmp_path)
        assert md.read_text(encoding="utf-8") == original
