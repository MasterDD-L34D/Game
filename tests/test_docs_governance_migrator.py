class TestRegistryHelpers:
    def test_load_save_round_trip(self, tmp_path: Path):
        reg = {"entries": [{"path": "docs/b.md"}, {"path": "docs/a.md"}]}
        path = tmp_path / "registry.json"
        path.write_text(json.dumps(reg), encoding="utf-8")

        loaded = migrator.load_registry(path)
        migrator.save_registry(path, loaded)
        reloaded = json.loads(path.read_text(encoding="utf-8"))
        # Entries should be sorted by path after save
        assert reloaded["entries"][0]["path"] == "docs/a.md"
        assert reloaded["entries"][1]["path"] == "docs/b.md"

    def test_registry_paths(self):
        reg = {"entries": [{"path": "a.md"}, {"path": "b.md"}, {"no_path": True}]}
        assert migrator.registry_paths(reg) == {"a.md", "b.md"}


