class TestParseFrontmatter:
    def test_valid_frontmatter(self, tmp_path: Path):
        md = tmp_path / "doc.md"
        md.write_text("---\ntitle: Hello\ndoc_status: active\n---\n# Body\n", encoding="utf-8")
        fm = migrator.parse_frontmatter(md)
        assert fm is not None
        assert fm["title"] == "Hello"
        assert fm["doc_status"] == "active"

    def test_no_frontmatter(self, tmp_path: Path):
        md = tmp_path / "doc.md"
        md.write_text("# Just a heading\nSome text.\n", encoding="utf-8")
        assert migrator.parse_frontmatter(md) is None

    def test_boolean_and_int_parsing(self, tmp_path: Path):
        md = tmp_path / "doc.md"
        md.write_text("---\nsource_of_truth: true\nprimary: false\nreview_cycle_days: 14\n---\n", encoding="utf-8")
        fm = migrator.parse_frontmatter(md)
        assert fm["source_of_truth"] is True
        assert fm["primary"] is False
        assert fm["review_cycle_days"] == 14

    def test_utf8_bom(self, tmp_path: Path):
        md = tmp_path / "doc.md"
        md.write_bytes(b"\xef\xbb\xbf---\ntitle: BOM\n---\n# Content\n")
        fm = migrator.parse_frontmatter(md)
        assert fm is not None
        assert fm["title"] == "BOM"

    def test_unclosed_frontmatter_returns_none(self, tmp_path: Path):
        md = tmp_path / "doc.md"
        md.write_text("---\ntitle: Broken\nNo closing delimiter\n", encoding="utf-8")
        assert migrator.parse_frontmatter(md) is None

    def test_nonexistent_file_returns_none(self, tmp_path: Path):
        assert migrator.parse_frontmatter(tmp_path / "missing.md") is None


