class TestCmdScan:
    def test_scan_writes_json_report(self, tmp_path: Path):
        docs = tmp_path / "docs"
        docs.mkdir()
        (docs / "test.md").write_text("---\ntitle: T\nworkstream: flow\n---\n# T\n", encoding="utf-8")

        report_path = tmp_path / "out" / "report.json"
        args = argparse.Namespace(command="scan", report=str(report_path))

        ret = migrator.cmd_scan(args, tmp_path)
        assert ret == 0
        assert report_path.exists()

        data = json.loads(report_path.read_text(encoding="utf-8"))
        assert data["summary"]["total"] >= 1
        assert any(f["path"] == "docs/test.md" for f in data["files"])


