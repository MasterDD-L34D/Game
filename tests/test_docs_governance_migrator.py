class TestCollectMdFiles:
    def test_collects_from_docs_reports_root(self, tmp_path: Path):
        (tmp_path / "docs").mkdir()
        (tmp_path / "docs" / "a.md").write_text("# A", encoding="utf-8")
        (tmp_path / "docs" / "sub").mkdir()
        (tmp_path / "docs" / "sub" / "b.md").write_text("# B", encoding="utf-8")
        (tmp_path / "reports").mkdir()
        (tmp_path / "reports" / "r.md").write_text("# R", encoding="utf-8")
        (tmp_path / "ROOT.md").write_text("# Root", encoding="utf-8")

        files = migrator.collect_md_files(tmp_path)
        rel_paths = {f.relative_to(tmp_path).as_posix() for f in files}
        assert "docs/a.md" in rel_paths
        assert "docs/sub/b.md" in rel_paths
        assert "reports/r.md" in rel_paths
        assert "ROOT.md" in rel_paths


